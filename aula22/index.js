/* 
    Operadores Lógicos

    && AND -> E (Todas as expressões precisam ser verdadeiras para retornar true)
    || OR -> OU
    ! NOT -> NÃO

*/

const expressaoAnd = true && true && false && true;
console.log(expressaoAnd);
const expressaoOr = true || true || false || true;
console.log(expressaoOr);